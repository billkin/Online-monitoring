/**
*【设置SPI速率】
*分频系数：2-256分频
*/
void SPI_SetSpeed(SPI_HandleTypeDef *spiHandle,uint32_t SPI_BaudRatePrescaler)
{
	assert_param(IS_SPI_BAUDRATE_PRESCALER(SPI_BaudRatePrescaler));//判断有效性
	__HAL_SPI_DISABLE(spiHandle);            //关闭SPI
	spiHandle->Instance->CR1&=0XFFC7;          //位3-5清零，用来设置波特率
	spiHandle->Instance->CR1|=SPI_BaudRatePrescaler;//设置SPI速度
	__HAL_SPI_ENABLE(spiHandle);             //使能SPI
}

uint8_t SPI_ReadWriteByte(SPI_HandleTypeDef *spiHandle,uint8_t TData)   //通过SPI读写一个字节
{
  uint8_t	RxData;
	if(HAL_SPI_TransmitReceive(spiHandle,&TData,&RxData,1,0xff)!=HAL_OK)
     RxData=0xFF; 
  return RxData;
}
